    public void mostrarDatos() {
        Conexion cc=new Conexion();
        Connection con=cc.getConnection();
        
        String [] titulos={"Cedula","Nombres","Apellidos","Celular","Correo","Direccion","Genero","Fecha de Nacimiento"};
        String [] registros=new String [8];
        
        DefaultTableModel modelo= new DefaultTableModel(null, titulos);
        
        String SQL="select* from administrativo";
        
        try {
            
           Statement st=con.createStatement();
           ResultSet rsu=st.executeQuery(SQL);
           
            while (rsu.next()) {
                
                registros [0]= rsu.getString("ID_ADMI");
                registros [1]= rsu.getString("ADMI_NOMBRE");
                registros [2]= rsu.getString("ADMI_APELLIDO");
                registros [3]= rsu.getString("ADMI_CELULAR");
                registros [4]= rsu.getString("ADMI_CORREO");
                registros [5]= rsu.getString("ADMI_DIRECCION");
                registros [6]= rsu.getString("ADMI_SEXO");
                registros [7]= rsu.getString("ADMI_FECHA_NACIMIENTO");
                
            modelo.addRow(registros);
            }
           tablaconsulta.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar los datos del perosnla administrativo" + e.getMessage());
        }
    }
