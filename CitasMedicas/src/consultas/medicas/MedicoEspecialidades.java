        Connection con = null;
        
        try{
            
            con = getConection();
            ps = con.prepareStatement("INSERT INTO medicoespecialidad(Medico_ID_MEDICO, MedicoEspecialidad_ID_MEDESPE) VALUES(?,?)");
            ps.setString(1, cboxMedID.getSelectedItem().toString());
            ps.setString(2, cboxEsp.getSelectedItem().toString());
           
            
            
            int res = ps.executeUpdate();
            
            if(res > 0){
            JOptionPane.showMessageDialog(null, "Medico Guardado");
            limpiarCajas();
            } else{
                JOptionPane.showMessageDialog(null,"Error al guardar med");
           limpiarCajas();
            }
            con.close();
            
        } catch(Exception e){
            System.err.println(e);
            
        }
        
        
